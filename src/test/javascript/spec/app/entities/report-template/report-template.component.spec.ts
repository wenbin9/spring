import { ComponentFixture, TestBed } from '@angular/core/testing';
import { of } from 'rxjs';
import { HttpHeaders, HttpResponse } from '@angular/common/http';
import { ActivatedRoute, convertToParamMap } from '@angular/router';

import { SpringTestModule } from '../../../test.module';
import { ReportTemplateComponent } from 'app/entities/report-template/report-template.component';
import { ReportTemplateService } from 'app/entities/report-template/report-template.service';
import { ReportTemplate } from 'app/shared/model/report-template.model';

describe('Component Tests', () => {
  describe('ReportTemplate Management Component', () => {
    let comp: ReportTemplateComponent;
    let fixture: ComponentFixture<ReportTemplateComponent>;
    let service: ReportTemplateService;

    beforeEach(() => {
      TestBed.configureTestingModule({
        imports: [SpringTestModule],
        declarations: [ReportTemplateComponent],
        providers: [
          {
            provide: ActivatedRoute,
            useValue: {
              data: of({
                defaultSort: 'id,asc',
              }),
              queryParamMap: of(
                convertToParamMap({
                  page: '1',
                  size: '1',
                  sort: 'id,desc',
                })
              ),
            },
          },
        ],
      })
        .overrideTemplate(ReportTemplateComponent, '')
        .compileComponents();

      fixture = TestBed.createComponent(ReportTemplateComponent);
      comp = fixture.componentInstance;
      service = fixture.debugElement.injector.get(ReportTemplateService);
    });

    it('Should call load all on init', () => {
      // GIVEN
      const headers = new HttpHeaders().append('link', 'link;link');
      spyOn(service, 'query').and.returnValue(
        of(
          new HttpResponse({
            body: [new ReportTemplate(123)],
            headers,
          })
        )
      );

      // WHEN
      comp.ngOnInit();

      // THEN
      expect(service.query).toHaveBeenCalled();
      expect(comp.reportTemplates && comp.reportTemplates[0]).toEqual(jasmine.objectContaining({ id: 123 }));
    });

    it('should load a page', () => {
      // GIVEN
      const headers = new HttpHeaders().append('link', 'link;link');
      spyOn(service, 'query').and.returnValue(
        of(
          new HttpResponse({
            body: [new ReportTemplate(123)],
            headers,
          })
        )
      );

      // WHEN
      comp.loadPage(1);

      // THEN
      expect(service.query).toHaveBeenCalled();
      expect(comp.reportTemplates && comp.reportTemplates[0]).toEqual(jasmine.objectContaining({ id: 123 }));
    });

    it('should calculate the sort attribute for an id', () => {
      // WHEN
      comp.ngOnInit();
      const result = comp.sort();

      // THEN
      expect(result).toEqual(['id,desc']);
    });

    it('should calculate the sort attribute for a non-id attribute', () => {
      // INIT
      comp.ngOnInit();

      // GIVEN
      comp.predicate = 'name';

      // WHEN
      const result = comp.sort();

      // THEN
      expect(result).toEqual(['name,desc', 'id']);
    });
  });
});
